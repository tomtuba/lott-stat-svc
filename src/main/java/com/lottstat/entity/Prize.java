package com.lottstat.entity;

public class Prize {
	private int value;
	private int totalPrizes;
	private int remainingPrizes;
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	public int getTotalPrizes() {
		return totalPrizes;
	}
	public void setTotalPrizes(int totalPrizes) {
		this.totalPrizes = totalPrizes;
	}
	public int getRemainingPrizes() {
		return remainingPrizes;
	}
	public void setRemainingPrizes(int remainingPrizes) {
		this.remainingPrizes = remainingPrizes;
	}
	

}
